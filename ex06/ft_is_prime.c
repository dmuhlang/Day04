#include <stdio.h>

int	down_sqrt(int nb)
{
	int r;

	if (nb < 0)
		return (-2);
	if (nb <= 1)
		return (-1);
	r = 2;
	while (r * r <= nb)
	{
		if (r * r == nb)
			return (-1);
		r++;
	}
	return (r - 1);
}


